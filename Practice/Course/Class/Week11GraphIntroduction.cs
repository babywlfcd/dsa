        // Graphs are a collection of nodes and edges that connect these nodes.
        // Graphs can be directed or undirected.
        // Graphs can be weighted or unweighted.
        // Graphs can be cyclic or acyclic.
        // Graphs can be connected or disconnected.
        // Graphs can be represented in two ways: Adjacency Matrix and Adjacency List.
        // Adjacency Matrix is a 2D array of size V x V where V is the number of vertices in the graph.
        // Adjacency List is an array of linked lists where the size of the array is equal to the number of vertices in the graph.
        // The most common graph traversal algorithms are Breadth First Search (BFS) and Depth First Search (DFS).
        // BFS is used to find the shortest path between two nodes.
        // DFS is used to find connected components in a graph.
        // BFS and DFS can be implemented using both Adjacency Matrix and Adjacency List.
        // BFS and DFS have a time complexity of O(V + E) where V is the number of vertices and E is the number of edges in the graph.
        // BFS and DFS have a space complexity of O(V) where V is the number of vertices in the graph.
        // BFS uses a queue data structure to keep track of the nodes to visit next.
        // DFS uses a stack data structure to keep track of the nodes to visit next.
        // BFS and DFS can be used to solve a variety of graph problems such as finding the shortest path, detecting cycles, and finding connected components.
        // BFS and DFS are fundamental algorithms in graph theory and are used in a wide range of applications such as social networks, routing algorithms, and recommendation systems.

